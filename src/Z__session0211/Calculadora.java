package Z__session0211;

public class Calculadora {
    public int suma(int x1, int x2){
        int resultado;
        resultado =x1+x2;
        return resultado;
    }


    public int resta (int x1, int x2){
        int resultado;
        resultado = x1-x2;
        return resultado;
    }

    public int multiplicacion (int x1, int x2){
        int resultado;
        resultado = -x1*x2;
        return resultado;
    }

    public int division (int x1, int x2){
        int resultado;
        resultado = -x1/x2;
        return resultado;
    }


}






